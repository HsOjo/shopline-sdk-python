"""Shopline API 数据模型 - Affiliate"""

from typing import Any, Dict, List, Optional, Union
from pydantic import BaseModel, Field
from typing_extensions import Literal



class ConfigurationConfig(BaseModel):
    """Configuration model for configuration"""
    offer_id: Optional[str] = None
    advertiser_id: Optional[str] = None
    commission_amount: Optional[str] = None
    default_category: Optional[str] = None
    site: Optional[str] = None
    shopid: Optional[str] = None
    authkey: Optional[str] = None
    category_mappings: Optional[Dict[str, str]] = None

class Affiliate(BaseModel):
    id: Optional[str] = None
    """Affiliate ID (Generated by SHOPLINE automatically) 合作廠商 ID (由 SHOPLINE 自動產生)"""
    affiliate_name: Optional[str] = None
    """Affiliate name 合作廠商名稱"""
    configuration: Optional[ConfigurationConfig] = None
    """Configuration 合作廠商設定"""
    enabled: Optional[bool] = None
    """Determine the affiliate is enabled or not 合作廠商是否啟用"""
    created_at: Optional[str] = None
    """Affiliate created time 建立時間"""
    updated_at: Optional[str] = None
    """Affiliate updated time 更新時間"""